heart = pd.read_csv("heart_disease_dataset.csv")
stroke = pd.read_csv("healthcare-dataset-stroke-data.csv")

heart = heart.dropna()

heart = heart.sample(n=80, random_state=42)

heart['stroke'] = 0

stroke = stroke.drop(columns=['id'])

stroke['bmi'].fillna(stroke['bmi'].mean(), inplace=True)

stroke = pd.get_dummies(stroke, drop_first=True)

stroke = stroke.sample(n=80, random_state=42)

stroke['heart_disease'] = 0

heart_cols = set(heart.columns)
stroke_cols = set(stroke.columns)

missing_in_heart = stroke_cols - heart_cols
missing_in_stroke = heart_cols - stroke_cols

for col in missing_in_heart:
    heart[col] = 0

for col in missing_in_stroke:
    stroke[col] = 0

stroke = stroke[heart.columns]

combined = pd.concat([heart, stroke], ignore_index=True)

combined.shape

combined['disease'] = 0

combined.loc[
    (combined['heart_disease'] == 1) & (combined['stroke'] == 0),
    'disease'
] = 1

combined.loc[
    (combined['heart_disease'] == 0) & (combined['stroke'] == 1),
    'disease'
] = 2

combined.loc[
    (combined['heart_disease'] == 1) & (combined['stroke'] == 1),
    'disease'
] = 3

X = combined.drop(columns=['disease'])

y = combined['disease']

scaler = StandardScaler()

X_scaled = scaler.fit_transform(X)

X_train, X_test, y_train, y_test = train_test_split(
    X_scaled, y, test_size=0.2, random_state=42
)

ridge_model = Ridge(alpha=1.0)

ridge_model.fit(X_train, y_train)

y_pred = ridge_model.predict(X_test)

y_pred = np.round(y_pred).astype(int)

y_pred = np.clip(y_pred, 0, 3)

print("Accuracy:", accuracy_score(y_test, y_pred))

print(classification_report(y_test, y_pred))

cm = confusion_matrix(y_test, y_pred)

